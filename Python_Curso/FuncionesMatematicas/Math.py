import math # Importamos la libreria matematica

PI = -3.14
x = 2
y = 23
z = -8

print(round(PI)) # Redondea hacia abajo un numero
print(math.ceil(PI)) # Redondea hacia arriba un numero
print(math.abs(PI)) # Nos da el valor absoluto del numero
print(math.pow(PI, 2)) # Nos da la potencia de un numero
print(math.sqrt(423215)) # Encuentra la raiz cuadrada de un numero
print(math.max(x,y,z)) # Nos da el nunero mayor entre los datos que le ingresemos
print(math.min(x,y,z)) # Nos da el numero menor entre los datos que le ingresemos