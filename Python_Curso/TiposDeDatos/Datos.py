nombre = "Ignacio" # String (cadena de texto)
apellido = "Frachia" # String (cadena de texto)
nombre_completo = nombre + " " + apellido # Concatenación de strings
edad = 24 # Dato entero (entero)
altura = 1.70 # Dato decimal (float)
tengo_mascota = True
print(f"Mi nombre es: {nombre} {apellido}, tengo {edad} años y tengo una altura de {altura} metros")
print(f"Ahora, solo mi nombre completo es: {nombre_completo}")
print(f"Tengo mascota? {tengo_mascota}")

# Imprime el tipo de dato de cada variable
print(type(nombre)) # String (cadena de texto)
print(type(apellido)) # String (cadena de texto)
print(type(edad)) # Dato entero (entero)
print(type(altura)) # Dato de cimal (float)
print(type(tengo_mascota)) # Dato booleano (boolean)

