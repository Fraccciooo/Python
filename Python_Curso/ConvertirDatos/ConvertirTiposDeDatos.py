x = 1
y = 2.0
z = "34"

print(f"Valor de x: {x}, Valor de y: {y}, Valor de z: {z}")

# -----------------------------------------------------------
# Convertir a entero
x = int(x)
y = int(y)
z = int(z)

# Estos son los valores despues de hacer la conversion 
print(f"Valor de x: {x}, Valor de y: {y}, Valor de z: {z}")

# -----------------------------------------------------------
# Convertir a float
x = float(x)
y = float(y)
z = float(z)

# Estos son los valores despues de hacer la conversion 
print(f"Valor de x: {x}, Valor de y: {y}, Valor de z: {z}")

# -----------------------------------------------------------
# Convertir a cadena
x = str(x)
y = str(y)
z = str(z)

# Estos son los valores despues de hacer la conversion 
print(f"Valor de x: {x}, Valor de y: {y}, Valor de z: {z}")